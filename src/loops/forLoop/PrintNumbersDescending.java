package loops.forLoop;

public class PrintNumbersDescending {
    public static void main(String[] args) {
        /*
        from 100 to 0

         Decide:
            start point : 100
            end point   : 0
            update      : decrement
         */

        for(int i = 100; i >= 0; i--){
            System.out.println(i);
        }
    }
}
