package operators.shortHandAssignmentOperators;

public class Example {
    public static void main(String[] args) {

        int age = 5;
        System.out.println(age);

        age += 3;   //age =age+3;
        System.out.println(age);

        age -= 2;
        System.out.println(age);

        age *= 2;
        System.out.println(age);

        age /=3;
        System.out.println(age);

        age%=3;
        System.out.println(age);


    }
}
