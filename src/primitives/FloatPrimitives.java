package primitives;

public class FloatPrimitives {
    public static void main(String[] args){
        //decimal numbers
        float myFirstFloat = 14.5F;
        float mySecondFloat = 45.2F;
        float myThirdFloat = 56.2435547F;

        System.out.println(myFirstFloat);
        System.out.println(mySecondFloat);
        System.out.println(myThirdFloat);
    }
}
