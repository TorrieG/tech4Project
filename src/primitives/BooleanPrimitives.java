package primitives;

public class BooleanPrimitives {
    public static void main(String[] args){
        //true or false statement
        boolean myTrueValue = true; //1 binary value
        boolean myFalseValue = false; //0 binary value

        System.out.println(myFalseValue);
        System.out.println(myTrueValue);
        System.out.println(myTrueValue);
    }
}
